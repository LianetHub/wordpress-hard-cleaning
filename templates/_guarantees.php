 <script>
     document.addEventListener('DOMContentLoaded', () => {
         const navLinks = document.querySelectorAll('.g-nav-link');
         const tabContents = document.querySelectorAll('.g-tab-content');

         tabContents.forEach((tab, index) => {
             const step = String(index + 1).padStart(2, '0');
             tab.setAttribute('data-step', step);
             const title = tab.querySelector('h3');
             if (title) {
                 title.setAttribute('data-step', step);
             }
         });

         navLinks.forEach(link => {
             link.addEventListener('click', (e) => {
                 e.preventDefault(); // Чтобы страница не прыгала вверх

                 const targetId = link.getAttribute('data-target');

                 // 1. Убираем активный класс у всех ссылок
                 navLinks.forEach(l => l.classList.remove('active'));
                 // 2. Убираем активный класс у всех блоков текста
                 tabContents.forEach(c => c.classList.remove('active'));

                 // 3. Добавляем активный класс текущей ссылке
                 link.classList.add('active');
                 // 4. Показываем нужный блок текста
                 document.getElementById(targetId).classList.add('active');
             });
         });
     });
 </script>